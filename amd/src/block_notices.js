export const init = () => {
  /* global Swiper */
  /* eslint no-undef: "error" */
    new Swiper('.swiper', {
        direction: 'horizontal',
        spaceBetween: 30,
        autoplay: {
            delay: 5000,
            pauseOnMouseEnter: true,
            disableOnInteraction: true,
        },
        loop: true,

        // We need pagination
        pagination: {
          el: '.swiper-pagination',
        },

        // Navigation arrows
        navigation: {
          nextEl: '.swiper-button-next',
          prevEl: '.swiper-button-prev',
        },
      });
};